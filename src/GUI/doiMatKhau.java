    private boolean checkTrong() {
       if (txtpasscu.getText().length() == 0) {
            MsgBox.alert(this, "Mời nhập mật khẩu cũ ! ");
            return false;
        }
        if (txtpassmoi.getText().length() == 0) {
            MsgBox.alert(this, "Không để trống mật khẩu mới !");
            return false;
        }
        if (txtxacnhan.getText().length() == 0) {
            MsgBox.alert(this, "Xác nhận mật khẩu trống! ");
            return false;
        }
        return true;
    }
