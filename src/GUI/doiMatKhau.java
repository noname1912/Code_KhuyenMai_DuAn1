    private void btnxacnhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxacnhanActionPerformed
      if (checkTrong() == false) {
            return;
        }
        String taiKhoan = txttaikhoan.getText();
        String matKhau = txtpasscu.getText();
        String matKhauMoi1 = txtpassmoi.getText();
        String matKhauMoi2 = txtxacnhan.getText();

        if (!matKhau.equals(Auth.user.getMatKhau())) {
            MsgBox.alert(this, "Nhập sai mật khẩu cũ !");
            return;
        }
        if (!matKhauMoi2.equals(matKhauMoi1)) {
            MsgBox.alert(this, "Mật khẩu mới không khớp !" + "\n" + " Mời nhập lại: ");
            return;
        }

        try {
            int row = JDBC_Helper.update("UPDATE NHANVIEN\n"
                    + "SET    MATKHAU = ?\n"
                    + "WHERE  MANHANVIEN = ?", matKhauMoi2, Auth.user.getMaNhanVien());
            if (row > 0) {
                MsgBox.alert(this, "Đổi mật khẩu thành công!");
                clearForm();
            }
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "Lỗi đổi mật khẩu!");
        }
    }//GEN-LAST:event_btnxacnhanActionPerformed

